#include "libft.h"

int	ft_atoi(const char	*str)
{
	unsigned int	n;
	unsigned int	sign;

	n = 0;
	sign = 1;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-')
		sign *= -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
		n = n * 10 + *(str++) - '0';
	return (n * sign);
}
